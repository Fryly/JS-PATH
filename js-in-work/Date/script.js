const time = new Date();
console.log(time);//текущая дата + время, до милисекунд в формате YYYY-MM-DDTHH:MM:SS.MLSZ

//если мы зададим какое-то число(количество милисекунд), то мы получим даты считая от 1970 года 1 января
//к примеру если зададим 0, то получим тот самый 1970 год 1 января
//по другому это число которое мы указываем называет тайм стамп
